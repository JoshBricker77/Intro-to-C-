#include <iostream>

int main() {
    int myVariable = 10; // Declare an integer variable and initialize it with a value of 10
    
    myVariable = myVariable + 1; // Add 1 to the variable
    
    // Output the result to the console
    std::cout << "The value of myVariable after adding 1 is: " << myVariable << std::endl;
    
    return 0;
}